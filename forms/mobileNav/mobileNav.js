hmbMenu.onclick=function(s){
  switch(s) {
  case "Dessert Voting":
    ChangeForm(dessertVoting)
    break;
  case "Describing You":
    ChangeForm(describeYou)
    break;
  case "loginCU":
    ChangeForm(loginCU)
    break;
  case "Exercise Knowledge":
    ChangeForm(favExercises)
    break;
  case "Favorite Foods":
    ChangeForm(favFoods)
    break;
  default:

}
}
